import java.util.Scanner;

public class HotShortKeys {
    public static void main(String[] args) {

        // Alt+Enter ...

        // Ctrl+Alt+M (Extract method)=>
        // create new method with selected code

        // Ctrl+Alt+V (Extract expression) =>
        // create new variable with selected expression

        // Ctrl+C -> copy
        // Ctrl+D -> copy line or selected code
        // Ctrl+D -> copy line or selected code
        // Ctrl+D -> copy line or selected code
        // Ctrl+D -> copy line or selected code

        // Ctrl+V -> put last copied part of buffer
        // Ctrl+Shift+V -> select and put copied part of buffer (on Intellij Idea)
        // Windows+V -> select and put copied part of buffer (on Windows)

        // Alt+J: find match parts
        int num10 = 20;

        // [Fn]+Shift+F6 : rename variable or file name
        int number = 10;
        int num2 = 20;
        System.out.println("number = " + number);
        System.out.println("num2 = " + num2);

        // Ctrl+/ => single line comment/uncomment
        // Ctrl+Shift+/ => multiline line comment/uncomment

        // Ctrl+F => find in this file
        // Ctrl+Shift+F => find  by project or module or directory or scope

        // Ctrl+R => replace in this file
        // Ctrl+Shift+R => replace  by project or module or directory or scope

        // (Ctrl | Alt) + Shift + (Up | Down) => move selected lines

        // Ctrl+Alt+L => format code style

        // Alt+Shift + "put cursor" => put cursor anywhere
        // Alt (press)+"up cursor | down cursor" => put cursor by column
    }
}
